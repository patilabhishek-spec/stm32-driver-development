/************************************************************
 * @fn         I2C_PeriClockControl
 *
 * @brief      Enables or disables the peripheral clock for
 *             the specified I2C peripheral.
 *
 * @param[in]  pI2Cx  : Base address of the I2C peripheral
 *                      (e.g., I2C1, I2C2, I2C3)
 *
 * @param[in]  EnorDi : Enable or Disable macro
 *                      ENABLE  - Enables I2C peripheral clock
 *                      DISABLE - Disables I2C peripheral clock
 *
 * @return     None
 *
 * @note       This function configures the corresponding
 *             I2C clock in the RCC (Reset and Clock Control)
 *             register. The peripheral clock must be enabled
 *             before configuring the I2C registers.
 *
 ************************************************************/

void I2C_PeriClockControl(I2C_RegDef_t *pI2Cx, uint8_t EnorDi) {
	if (EnorDi == ENABLE) {
		if (pI2Cx == I2C1) {
			I2C1_PCLK_EN();
		} else if (pI2Cx == I2C2) {
			I2C2_PCLK_EN();
		} else if (pI2Cx == I2C3) {
			I2C3_PCLK_EN();
		}

	} else {
		if (pI2Cx == I2C1) {
			I2C1_PCLK_DI();
		} else if (pI2Cx == I2C2) {
			I2C2_PCLK_DI();
		} else if (pI2Cx == I2C3) {
			I2C3_PCLK_DI();
		}
	}

}

/************************************************************
 * @fn         I2C_Init
 *
 * @brief      Initializes the I2C peripheral with the
 *             specified configuration settings.
 *
 * @param[in]  pI2CHandle : Pointer to the I2C handle structure
 *                          which contains the base address of
 *                          the I2C peripheral and configuration
 *                          parameters.
 *
 * @return     None
 *
 * @note       This function configures various I2C registers
 *             such as CR1, CR2, OAR1, and CCR based on the
 *             configuration provided in the handle structure.
 *
 ************************************************************/

void I2C_Init(I2C_Handle_t *pI2CHandle) {

	I2C_PeriClockControl(pI2CHandle->pI2Cx, ENABLE);
	uint32_t tempreg = 0;
	tempreg |= (pI2CHandle->I2C_Config.I2C_AckControl << 10);
	pI2CHandle->pI2Cx->I2C_CR1 = tempreg;

	//configure the FREQ field of CR2
	tempreg = 0;
	tempreg |= RCC_GetPCLK1Value() / 1000000U; //converting from hz to mhz by dividing
	pI2CHandle->pI2Cx->I2C_CR2 = (tempreg & 0x3F);//“We mask with 0x3F to ensure only the valid 6-bit FREQ value is written into the CR2 register.

	//program the device own address  when stm32 act as slave
	tempreg = 0;
	tempreg |= pI2CHandle->I2C_Config.I2C_DeviceAddress << 1;
	tempreg |= (1 << 14);   //must be kept one if not divice dont work properly
	pI2CHandle->pI2Cx->I2C_OAR1 = tempreg;

	//CCR calculation   clock control register (how fast scl will run) high ccr -> slow clock low ccr->faster clock
	uint16_t ccr_value = 0;
	tempreg = 0;
	if (pI2CHandle->I2C_Config.I2C_SCL_Speed <= I2C_SCL_SPEED_SM) {
		//Mode Is Standard Mode
		ccr_value = (RCC_GetPCLK1Value()
				/ (2 * pI2CHandle->I2C_Config.I2C_SCL_Speed));
		tempreg |= (ccr_value & 0xFFF);
	} else {
		//Mode is fast mode
		tempreg |= (1 << 15);
		tempreg |= (pI2CHandle->I2C_Config.I2C_FMDutyCycle << 14);
		if (pI2CHandle->I2C_Config.I2C_FMDutyCycle == I2C_FM_DUTY_2) {
			ccr_value = (RCC_GetPCLK1Value()
					/ (3 * pI2CHandle->I2C_Config.I2C_SCL_Speed));
		} else {
			ccr_value = (RCC_GetPCLK1Value()
					/ (25 * pI2CHandle->I2C_Config.I2C_SCL_Speed));

		}

		tempreg |= (ccr_value & 0xFFF);
	}

	pI2CHandle->pI2Cx->I2C_CCR = tempreg;

	//TRISE configuration maximum rise time setting

	if (pI2CHandle->I2C_Config.I2C_SCL_Speed <= I2C_SCL_SPEED_SM) {
		//Mode Is Standard Mode
		tempreg = (RCC_GetPCLK1Value() / 1000000U) + 1;//Max rise time = 1000 ns (1 µs) 100000U=Tpclk=Tpclk1 = 1 / PCLK1
	} else {
		//mode is fast mode  TRISE = (300 ns / Tpclk1) + 1
		tempreg = (RCC_GetPCLK1Value() * 300 / 1000000000U) + 1;//Max rise time = 300 ns (fast mode)
	}

	// Write the rise time value to the I2C TRISE register.
	// Mask with 0x3F to keep only the lower 6 bits because TRISE register uses only 6 bits.
	pI2CHandle->pI2Cx->I2C_TRISE = (tempreg & 0x3F);
}

/************************************************************
 * @fn         I2C_DeInit
 *
 * @brief      Resets the selected I2C peripheral registers
 *             to their default reset values.
 *
 * @param[in]  pI2Cx : Base address of the I2C peripheral
 *
 * @return     None
 *
 * @note       This function performs a peripheral reset using
 *             the RCC reset register.
 *
 ************************************************************/

void I2C_DeInit(I2C_RegDef_t *pI2Cx) {
	if (pI2Cx == I2C1) {
		I2C1_REG_RESET();
	} else if (pI2Cx == I2C2) {
		I2C2_REG_RESET();
	} else if (pI2Cx == I2C3) {
		I2C3_REG_RESET();
	}

}

/************************************************************
 * @fn         I2C_GetFlagStatus
 *
 * @brief      Checks whether a specified I2C status flag is set or not.
 *
 * @param[in]  pI2Cx    : Pointer to the base address of the
 *                        I2C peripheral (I2C1, I2C2, I2C3)
 *
 * @param[in]  FlagName : Specifies the flag to check.
 *                        This parameter can be one of the
 *                        I2C status flags (e.g., I2C_FLAG_TXE,
 *                        I2C_FLAG_RXNE, I2C_FLAG_SB, etc.)
 *
 * @return     FLAG_SET   : If the specified flag is set
 *             FLAG_RESET : If the specified flag is cleared
 *
 * @note       This function reads the I2C status register (SR1)
 *             and performs a bitwise AND operation with the
 *             given flag to determine its status.
 *
 *             The status flags are set and cleared by hardware
 *             events; this function only reads the flag status.
 *
 ************************************************************/

uint8_t I2C_GetFlagStatus(I2C_RegDef_t *pI2Cx, uint32_t FlagName) {
	if (pI2Cx->I2C_SR1 & FlagName) {
		return FLAG_SET;
	}

	return FLAG_RESET;
}

/************************************************************
 * @fn         I2C_MasterSendData
 *
 * @brief      Sends data from master to a slave device over I2C.
 *
 * @param[in]  pI2CHandle : Pointer to the I2C handle structure
 *                          containing configuration and base
 *                          address of the I2C peripheral
 *
 * @param[in]  pTxBuffer  : Pointer to the transmit buffer
 *                          containing data to be sent
 *
 * @param[in]  Len        : Length of data to be transmitted
 *
 * @param[in]  SlaveAddr  : 7-bit address of the slave device
 *
 * @return     None
 *
 * @note       This is a blocking (polling-based) API.
 *             The function performs the following steps:
 *
 *             1. Generates the START condition
 *             2. Sends the slave address with write request
 *             3. Waits for address phase completion (ADDR flag)
 *             4. Clears ADDR flag by reading SR1 and SR2
 *             5. Sends data byte-by-byte
 *             6. Waits for TXE (data register empty)
 *             7. Waits for BTF (byte transfer finished)
 *             8. Generates STOP condition
 *
 *             All status flags (SB, ADDR, TXE, BTF) are
 *             handled using polling.
 *
 ************************************************************/

void I2C_MasterSendData(I2C_Handle_t *pI2CHandle, uint8_t *pTxBuffer,
		uint8_t Len, uint8_t SlaveAddr, uint8_t sr) {
	//1 Genrate a start condition
	I2C_GenerateStartCondition(pI2CHandle->pI2Cx);

	//2.confirm that start genration is completed by cheaking the SB flag in the SR1
	// Note: untill SB is cleared SCL will be streched (pulled to low)

	while (!I2C_GetFlagStatus(pI2CHandle->pI2Cx, I2C_FLAG_SB))
		;

	//3 send the address of the slave with r/w bit set to w(0) (total 8 bits)
	I2C_ExecuteAddressPhaseWrite(pI2CHandle->pI2Cx, SlaveAddr);

	//4.confirm that address phase is completed by cheaking the addr flag in the SR1
	while (!I2C_GetFlagStatus(pI2CHandle->pI2Cx, I2C_FLAG_ADDR))
		;

	//5.clear the ADDR flag accorrding to its softwere sequence
	//Note: untill ADDR is cleared SCL will be stretched(pulled to low)
	I2C_ClearADDRFlag(pI2CHandle);

	//6. send the data untill len becomes 0

	while (Len > 0) {
		while (!I2C_GetFlagStatus(pI2CHandle->pI2Cx, I2C_FLAG_TXE))
			;	 // wait until TXE is set means data register is empty
		pI2CHandle->pI2Cx->I2C_DR = *pTxBuffer;
		pTxBuffer++;
		Len--;
	}

	//7.when len becomes zero wait for txe=1 and btf=1 before gernarating the stop condition
	//Note. TXE =1,BTF=1,means that both SR and DR are empty and next transmission should begin
	//when BTF=1 SCL will be streched (pulled to low)

	while (!I2C_GetFlagStatus(pI2CHandle->pI2Cx, I2C_FLAG_TXE))
		;

	while (!I2C_GetFlagStatus(pI2CHandle->pI2Cx, I2C_FLAG_BTF))
		;

	//8. Genrate stop condition and master need to wait for the completion of stop condition
	// Note.genrating stop,automatically clears the btf

	if (sr == I2C_DISABLE_SR)
		I2C_GenerateStopCondition(pI2CHandle->pI2Cx);

}

/************************************************************
 * @fn         I2C_MasterReceiveData
 *
 * @brief      Receives data from a slave device over I2C
 *             in master mode.
 *
 * @param[in]  pI2CHandle : Pointer to the I2C handle structure
 *                          containing configuration and base
 *                          address of the I2C peripheral
 *
 * @param[out] pRxBuffer  : Pointer to the buffer where received
 *                          data will be stored
 *
 * @param[in]  Len        : Number of bytes to be received
 *
 * @param[in]  SlaveAddr  : 7-bit address of the slave device
 *
 * @return     None
 *
 * @note       This is a blocking (polling-based) API.
 *             The function performs the following steps:
 *
 *             1. Generates the START condition
 *             2. Sends the slave address with read request
 *             3. Waits for ADDR flag (address phase complete)
 *             4. Clears ADDR flag by reading SR1 and SR2
 *
 *             If Len == 1:
 *                - Disable ACK
 *                - Clear ADDR
 *                - Generate STOP
 *                - Wait for RXNE
 *                - Read data
 *
 *             If Len > 1:
 *                - Enable ACK
 *                - For each byte:
 *                    • Wait for RXNE
 *                    • Read data
 *                - Before last byte:
 *                    • Disable ACK
 *                    • Generate STOP
 *
 *             RXNE flag indicates data available in DR.
 *
 *             ACK control is critical to properly terminate
 *             reception and avoid extra bytes from slave.
 *
 ************************************************************/

void I2C_MasterReceiveData(I2C_Handle_t *pI2CHandle, uint8_t *pRxBuffer,
		uint8_t Len, uint8_t SlaveAddr, uint8_t sr) {
	//1.Genrate the START condition
	I2C_GenerateStartCondition(pI2CHandle->pI2Cx);

	//2.confirm that start genration is completed by cheaking the SB flag in the SR1
	// Note: untill SB is cleared SCL will be streched (pulled to low)

	while (!I2C_GetFlagStatus(pI2CHandle->pI2Cx, I2C_FLAG_SB))
		;

	//3 send the address of the slave with r/w bit set to r(1) (total 8 bits)
	I2C_ExecuteAddressPhaseRead(pI2CHandle->pI2Cx, SlaveAddr);

	//4.confirm that address phase is completed by cheaking the addr flag in the SR1
	while (!I2C_GetFlagStatus(pI2CHandle->pI2Cx, I2C_FLAG_ADDR))
		;

	//procedure to read only 1 byte from slave
	if (Len == 1) {
		//disable the acking
		I2C_ManageAcking(pI2CHandle->pI2Cx, I2C_ACK_DISABLE);

		//clear the ADDR flag
		I2C_ClearADDRFlag(pI2CHandle);

		//wait until RXNE becomes 1
		while (!I2C_GetFlagStatus(pI2CHandle->pI2Cx, I2C_FLAG_RXNE))
			;	// wait until RXNE is set means data register is empty

		//genrate STOP condition
		if (sr == I2C_DISABLE_SR)
			I2C_GenerateStopCondition(pI2CHandle->pI2Cx);

		//read data in to buffer

		*pRxBuffer = pI2CHandle->pI2Cx->I2C_DR;

	}

	//procedure to read data from slave when  (Len > 1)

	if (Len > 1) {
		//clear the ADDR flag
		I2C_ClearADDRFlag(pI2CHandle);

		//read the data untill Len becomes zero
		for (uint32_t i = Len; i > 0; i--) {
			//wait untill Rxne becomes 1
			while (!I2C_GetFlagStatus(pI2CHandle->pI2Cx, I2C_FLAG_RXNE))
				;	// wait until RXNE is set means data register is empty

			if (i == 2) //if last 2 bytes are remaining
					{
				//clear the ack bit
				I2C_ManageAcking(pI2CHandle->pI2Cx, I2C_ACK_DISABLE);

				//genrate the STOP condition
				if (sr == I2C_DISABLE_SR)
					I2C_GenerateStopCondition(pI2CHandle->pI2Cx);

			}

			//read the data from the data register in to buffer
			*pRxBuffer = pI2CHandle->pI2Cx->I2C_DR;

			//increment the buffer
			pRxBuffer++;

		}

	}

//re-enable the acking
	if (pI2CHandle->I2C_Config.I2C_AckControl == I2C_ACK_ENABLE) {
		I2C_ManageAcking(pI2CHandle->pI2Cx, I2C_ACK_ENABLE);

	}

}

/************************************************************
 * @fn         I2C_IRQInterruptConfig
 *
 * @brief      Enables or disables the interrupt for the
 *             specified IRQ number.
 *
 * @param[in]  IRQNumber : IRQ number of the interrupt
 *
 * @param[in]  EnorDi    : Enable or Disable macro
 *                         ENABLE  - Enables interrupt
 *                         DISABLE - Disables interrupt
 *
 * @return     None
 *
 * @note       This function configures the interrupt enable
 *             registers of the NVIC controller.
 *
 ************************************************************/

void I2C_IRQInterruptConfig(uint8_t IRQNumber, uint8_t EnorDi) {

	if (EnorDi == ENABLE) {
		if (IRQNumber <= 31) {
			//Program For ISRE0 Register
			*NVIC_ISER0 |= (1 << IRQNumber);

		} else if (IRQNumber > 31 && IRQNumber < 64) {

			//Program For ISER1 Register (32 to 63 )
			*NVIC_ISER1 |= (1 << (IRQNumber % 32));

		} else if (IRQNumber >= 64 && IRQNumber < 96) {
			//Program For ISRE2 Register  (64 to 95 )
			*NVIC_ISER2 |= (1 << (IRQNumber % 64));

		}
	} else {

		if (IRQNumber <= 31) {
			//Program For ICER0) Register
			*NVIC_ICER0 |= (1 << IRQNumber);

		} else if (IRQNumber > 31 && IRQNumber < 64) {

			//Program For ICER1 Register (32 to 63 )
			*NVIC_ICER1 |= (1 << (IRQNumber % 32));

		} else if (IRQNumber >= 64 && IRQNumber < 96) {
			//Program For ICER2 Register  (64 to 95 )
			*NVIC_ICER2 |= (1 << (IRQNumber % 64));

		}
	}

}

/************************************************************
 * @fn         I2C_IRQPriorityConfig
 *
 * @brief      Configures the priority level of a given
 *             interrupt.
 *
 * @param[in]  IRQNumber   : IRQ number of the interrupt
 *
 * @param[in]  IRQPriority : Priority level to be assigned
 *
 * @return     None
 *
 * @note       Lower priority number indicates higher
 *             interrupt priority.
 *
 ************************************************************/

void I2C_IRQPriorityConfig(uint8_t IRQNumber, uint32_t IRQPriority) {
	uint8_t iprx = IRQNumber / 4;
	uint8_t iprx_section = IRQNumber % 4;
	uint8_t shift_amount = (8 * iprx_section) + (8 - NO_PR_BITS_IMPLEMENTED);
	// it jump to that ipr register address  :here 1st we are finding which ipr address is this and  then we are jumping to that register address by adding the (ipsr *4 ) (4 beacuse reg is of 32 bit) value to the base  address
	*(NVIC_PR_BASE_ADDR + iprx) |= (IRQPriority << shift_amount);

}

/************************************************************
 * @fn         I2C_PeripheralControl
 *
 * @brief      Enables or disables the I2C peripheral.
 *
 * @param[in]  pI2Cx  : Base address of the I2C peripheral
 *
 * @param[in]  EnorDi : Enable or Disable macro
 *                      ENABLE  - Enables I2C peripheral
 *                      DISABLE - Disables I2C peripheral
 *
 * @return     None
 *
 * @note       This function controls the PE (Peripheral Enable)
 *             bit in the I2C CR1 register.
 *
 ************************************************************/

void I2C_PeripheralControl(I2C_RegDef_t *pI2Cx, uint8_t EnorDi) {
	if (EnorDi == ENABLE) {
		pI2Cx->I2C_CR1 |= (1 << I2C_CR1_PE);
	} else {
		pI2Cx->I2C_CR1 &= ~(1 << I2C_CR1_PE);

	}
}

/************************************************************
 * @fn         I2C_ManageAcking
 *
 * @brief      Enables or disables ACK (acknowledge) control
 *             for the I2C peripheral.
 *
 * @param[in]  pI2Cx  : Pointer to the base address of the
 *                      I2C peripheral (I2C1, I2C2, I2C3)
 *
 * @param[in]  EnorDi : ENABLE  -> Enable ACK
 *                      DISABLE -> Disable ACK
 *
 * @return     None
 *
 * @note       ACK is used during I2C data reception to inform
 *             the transmitter (slave) whether more data is
 *             expected.
 *
 *             When ACK is enabled:
 *                - Master sends ACK after receiving each byte
 *                - Used when receiving multiple bytes
 *
 *             When ACK is disabled:
 *                - Master sends NACK after receiving a byte
 *                - Typically used before receiving the last byte
 *                  to indicate end of reception
 *
 *             This function configures the ACK bit in the CR1
 *             register of the I2C peripheral.
 *
 ************************************************************/

void I2C_ManageAcking(I2C_RegDef_t *pI2Cx, uint8_t EnorDi) {
	if (EnorDi == I2C_ACK_ENABLE) {
		// enable the ACK
		pI2Cx->I2C_CR1 |= (1 << I2C_CR1_ACK);

	} else {
		//disable the ACK
		pI2Cx->I2C_CR1 &= ~(1 << I2C_CR1_ACK);

	}
}

/*********************************************************************
 * @fn      		  - I2C_MasterSendDataIT
 *
 * @brief             - Initiates I2C master transmission in interrupt mode.
 *                      This function configures the I2C peripheral to send
 *                      data to a slave device without blocking the CPU.
 *                      The transmission is handled using I2C interrupts.
 *
 * @param[in]         - pI2CHandle : Pointer to I2C handle structure which
 *                                  contains configuration, buffer pointers,
 *                                  state information, and I2C peripheral base address
 *
 * @param[in]         - pTxBuffer  : Pointer to the transmit buffer which holds
 *                                  the data to be sent to the slave device
 *
 * @param[in]         - Len        : Total number of bytes to be transmitted
 *
 * @param[in]         - SlaveAddr  : 7-bit address of the slave device
 *                                  to which data will be transmitted
 *
 * @param[in]         - Sr         : Repeated start condition control
 *                                  I2C_ENABLE_SR  -> Repeated start will be generated
 *                                  I2C_DISABLE_SR -> Stop condition will be generated
 *                                                    after transmission completes
 *
 * @return            - Returns the previous state of the I2C peripheral:
 *                      I2C_READY       -> Peripheral is ready for communication
 *                      I2C_BUSY_IN_TX  -> Transmission is already in progress
 *
 * @Note              - This is a non-blocking (interrupt-based) API.
 *                      The function only initializes the transmission.
 *                      Actual data transfer is handled inside ISR.
 *
 *                      The following interrupts are used:
 *                      - TXE (Transmit buffer empty)
 *                      - BTF (Byte transfer finished)
 *                      - STOPF (Stop detection, if applicable)
 *                      - Error interrupts (Bus error, arbitration lost, etc.)
 *
 *                      Internal members updated:
 *                      - pTxBuffer  -> Stores transmit buffer address
 *                      - TxLen      -> Stores remaining data length
 *                      - TxRxState  -> Set to I2C_BUSY_IN_TX
 *                      - DevAddr    -> Stores slave address
 *
 *                      User must implement:
 *                      I2C_ApplicationEventCallback()
 *                      to handle transmission complete and error events.
 *
 *********************************************************************/
uint8_t I2C_MasterSendDataIT(I2C_Handle_t *pI2CHandle, uint8_t *pTxBuffer,
		uint8_t Len, uint8_t SlaveAddr, uint8_t Sr) {

	uint8_t busystate = pI2CHandle->TxRRxState;

	if ((busystate != I2C_BUSY_IN_TX) && (busystate != I2C_BUSY_IN_RX)) {
		pI2CHandle->pTxBuffer = pTxBuffer;
		pI2CHandle->TxLen = Len;
		pI2CHandle->TxRRxState = I2C_BUSY_IN_TX;
		pI2CHandle->DevAddr = SlaveAddr;
		pI2CHandle->sr = Sr;

		//Implement code to Generate START Condition
		I2C_GenerateStartCondition(pI2CHandle->pI2Cx);

		//Implement the code to enable ITBUFEN Control Bit
		pI2CHandle->pI2Cx->I2C_CR2 |= (1 << I2C_CR2_ITBUFEN);

		//Implement the code to enable ITEVFEN Control Bit
		pI2CHandle->pI2Cx->I2C_CR2 |= (1 << I2C_CR2_ITEVTEN);

		//Implement the code to enable ITERREN Control Bit
		pI2CHandle->pI2Cx->I2C_CR2 |= (1 << I2C_CR2_ITERREN);

	}

	return busystate;

}

