 void I2C_GenerateStartCondition(I2C_RegDef_t *pI2Cx) {
	pI2Cx->I2C_CR1 |= (1 << I2C_CR1_START);
}


/*********************************************************************
 * @fn      		  - I2C_ExecuteAddressPhaseWrite
 *
 * @brief             - Executes the address phase for I2C master write operation.
 *                      Sends the slave address along with write bit (0)
 *                      on the I2C bus.
 *
 * @param[in]         - pI2Cx     : Pointer to I2C peripheral registers
 * @param[in]         - SlaveAddr : 7-bit address of the slave device
 *
 * @return            - None
 *
 * @Note              - This function is called after generating START condition.
 *
 *                      It performs the following:
 *                      - Shifts slave address to align with address field
 *                      - Clears LSB (write operation → 0)
 *                      - Writes address into DR register
 *
 *********************************************************************/

static void I2C_ExecuteAddressPhaseWrite(I2C_RegDef_t *pI2Cx, uint8_t SlaveAddr) {
	SlaveAddr = SlaveAddr << 1;  //this make a space for the read write bit
	SlaveAddr &= ~(1); //slave addr is slave addr + r/w bit  //setting the 0th bit for means for the write
	pI2Cx->I2C_DR = SlaveAddr;
}


/**
 * @brief  Executes the address phase for I2C READ operation.
 *
 * @param  pI2Cx      Pointer to I2C peripheral registers
 * @param  SlaveAddr  7-bit slave address
 *
 * @details
 * The I2C protocol uses a 7-bit slave address followed by a 1-bit
 * Read/Write (R/W) control bit.
 *
 * This function:
 * 1. Left shifts the 7-bit slave address by 1 to make space for R/W bit.
 * 2. Sets the least significant bit (LSB) to 1 to indicate READ operation.
 * 3. Writes the final 8-bit value into the data register (DR),
 *    which initiates address transmission on the I2C bus.
 *
 * Example:
 *   SlaveAddr = 0x50 (0101 0000)
 *   After shift  -> 1010 0000
 *   After OR 1   -> 1010 0001 (0xA1)
 *
 * Final transmitted value = (SlaveAddr << 1) | 1
 *
 * Note:
 *   LSB = 0 → Write operation
 *   LSB = 1 → Read operation
 */

static void I2C_ExecuteAddressPhaseRead(I2C_RegDef_t *pI2Cx, uint8_t SlaveAddr) {
	SlaveAddr = SlaveAddr << 1;  //this make a space for the read write bit
	SlaveAddr |= 1; //slave addr is slave addr + r/w bit  //setting the 0th bit for means for the read
	pI2Cx->I2C_DR = SlaveAddr;
}

/*********************************************************************
 * @fn      		  - I2C_ClearADDRFlag
 *
 * @brief             - Clears the ADDR (Address Matched) flag in I2C.
 *                      This flag is set when address phase is completed.
 *
 * @param[in]         - pI2CHandle : Pointer to I2C handle structure
 *
 * @return            - None
 *
 * @Note              - ADDR flag must be cleared by reading SR1 followed
 *                      by reading SR2 register.
 *
 *                      This function handles both master and slave modes
 *                      and ensures proper clearing to continue communication.
 *
 *********************************************************************/

static void I2C_ClearADDRFlag(I2C_Handle_t *pI2CHandle) {
	uint32_t dummy_read;
	//cheal the device mode
	if (pI2CHandle->pI2Cx->I2C_SR2 & (1 << I2C_SR2_MSL)) {
		//device in master mode
		if (pI2CHandle->TxRRxState == I2C_BUSY_IN_RX) {
			if (pI2CHandle->RxSize == 1) {
				//1.first disable the ack
				I2C_ManageAcking(pI2CHandle->pI2Cx, DISABLE);

				//clear the addr flag(read sr1,read sr2)
				dummy_read = pI2CHandle->pI2Cx->I2C_SR1;
				dummy_read = pI2CHandle->pI2Cx->I2C_SR2;
				(void) dummy_read; //it means that the variable is unused so compiler not genrate an error

			}
		} else {
			//for master sending the data
			dummy_read = pI2CHandle->pI2Cx->I2C_SR1;
			dummy_read = pI2CHandle->pI2Cx->I2C_SR2;
			(void) dummy_read;
		}

	} else {
		//device is in slave mode
		dummy_read = pI2CHandle->pI2Cx->I2C_SR1;
		dummy_read = pI2CHandle->pI2Cx->I2C_SR2;
		(void) dummy_read;

	}

}




 void I2C_GenerateStopCondition(I2C_RegDef_t *pI2Cx) {
	pI2Cx->I2C_CR1 |= (1 << I2C_CR1_STOP);

}

